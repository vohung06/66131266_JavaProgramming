package gh;

public class SinhVien {
	private int soThuTu;
	private String hoTen;
	private int namSinh;
	private String gioiTinh;
	
	public SinhVien() {
		
	}
	
	public SinhVien(int soThuTu, String hoTen, int namSinh, String gioiTinh) {
		this.soThuTu = soThuTu;
		this.hoTen = hoTen;
		this.namSinh = namSinh;
		this.gioiTinh = gioiTinh;
	}
	
	
}
